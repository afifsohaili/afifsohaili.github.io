<script>
import anime from 'animejs'
import Chance from 'chance'
import geometry from 'triangle-vector-geometry'

import ProfileSidebar from '~/components/ProfileSidebar'
import ProfessionalExperience from '~/components/ProfessionalExperience'
import LeadershipExperience from '~/components/LeadershipExperience'
import PersonalProjects from '~/components/PersonalProjects'
import AdditionalPresentations from '~/components/AdditionalPresentations'

const chance = new Chance()

export default {
  layout: 'empty',
  components: {
    AdditionalPresentations,
    LeadershipExperience,
    ProfessionalExperience,
    PersonalProjects,
    ProfileSidebar
  },
  data () {
    return {
      showSvg: '0',
      showResume: false
    }
  },
  mounted () {
    const timeline = anime.timeline({
      targets: '#face-path polygon',
      easing: 'easeInOutQuad',
      elasticity: 1000,
      direction: 'reverse'
    })
    timeline
      .add({
        strokeDashoffset: [anime.setDashoffset, 0],
        delay: function(el, i) { return i * chance.floating({min: 0, max: 3}) },
        duration: 1000
      })
      .add({
        targets: '#face-path polygon',
        'stroke-width': 2.5,
        delay: function(el, i) { return i * 10 },
        duration: 50,
        offset: '-=2000'
      })
      .add({
        targets: '#face-path polygon',
        easing: 'easeInOutBack',
        rotate: (el) => {
          const [c1, c2, c3] = Array(el.points.length).fill().map((_, i) => {
            const x = (el.points[i].x)
            const y = (el.points[i].y)

            return [x, y]
          })
          const centroid = geometry.centroid(c1, c2, c3)
          el.style.transformOrigin = centroid.map(p => `${p - 150}px`).join(' ')
          return chance.natural({max: 360})
        },
        translateY: chance.natural({max: 1500}),
        delay: function(el, i) { return i * chance.floating({min: 0, max: 3}) },
        duration: 3500,
        offset: '-=3500'
      })
      .add({
        points: (el) => {
          const {points} = el
          return Array(points.length).fill().map((_, i) => {
            const newX = (points[i].x)
            const newY = (points[i].y + 3000)

            return [newX, newY].join(',')
          }).join(' ')
        },
        delay: function(el, i) { return i * chance.floating({min: 0, max: 2}) },
        duration: 400,
        offset: '-=3500'
      })
    setTimeout(() => this.showSvg = '1', 200)
    timeline.complete = () => this.showResume = true
  }
}
